    /// <summary>
    /// Получить текст справки по теме
    /// </summary>
    public string GetTopic(string topicName)
    {
        if (_helpTopics.TryGetValue(topicName.ToLower(), out string topic))
        {
            return topic;
        }
        return $"Тема '{topicName}' не найдена. Доступные темы: {string.Join(", ", _helpTopics.Keys)}";
    }

    /// <summary>
    /// Получить все доступные темы справки
    /// </summary>
    public List<string> GetAllTopics()
    {
        return _helpTopics.Keys.ToList();
    }

    /// <summary>
    /// Показать главное меню справки
    /// </summary>
    public string ShowHelpMenu()
    {
        StringBuilder sb = new StringBuilder();
        sb.AppendLine("ДОСТУПНЫЕ ТЕМЫ СПРАВКИ:");
        sb.AppendLine("========================");
        sb.AppendLine("main        - Главная информация");
        sb.AppendLine("numbers     - Типы чисел");
        sb.AppendLine("base_systems - Системы счисления");
        sb.AppendLine("fractions   - Рациональные дроби");
        sb.AppendLine("complex     - Комплексные числа");
        sb.AppendLine("memory      - Работа с памятью");
        sb.AppendLine("clipboard   - Буфер обмена");
        sb.AppendLine("functions   - Математические функции");
        sb.AppendLine("keyboard    - Управление с клавиатуры");
        sb.AppendLine("about       - О программе");
        sb.AppendLine("========================");
        sb.AppendLine("Введите название темы для получения информации.");
        return sb.ToString();
    }

    /// <summary>
    /// Поиск по содержимому справки
    /// </summary>
    public List<string> Search(string query)
    {
        List<string> results = new List<string>();
        string lowerQuery = query.ToLower();

        foreach (var topic in _helpTopics)
        {
            if (topic.Key.Contains(lowerQuery) || topic.Value.ToLower().Contains(lowerQuery))
            {
                results.Add(topic.Key);
            }
        }

        return results;
    }
}
